-- =============================================================================
-- TABLE: lists
-- =============================================================================
-- STEP 2: Create the "lists" table.
-- This table stores shopping lists created by users.
CREATE TABLE lists (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    owner_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    share_token UUID NOT NULL UNIQUE DEFAULT gen_random_uuid(),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_lists_owner_id FOREIGN KEY (owner_id) REFERENCES users(id) ON DELETE CASCADE
);

-- Add a comment to the "lists" table.
COMMENT ON TABLE lists IS 'Stores shopping lists created by users.';


-- =============================================================================
-- TABLE: items
-- =============================================================================
-- STEP 3: Create the "items" table.
-- This table stores individual items within a shopping list.
CREATE TABLE items (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    list_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(255),
    is_bought BOOLEAN NOT NULL DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_items_list_id FOREIGN KEY (list_id) REFERENCES lists(id) ON DELETE CASCADE
);

-- Add a comment to the "items" table.
COMMENT ON TABLE items IS 'Stores individual items on shopping lists.';


