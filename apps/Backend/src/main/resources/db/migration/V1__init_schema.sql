-- STEP 6: Create a function and trigger to update the "updated_at" timestamp on the "lists" table.
-- This ensures that the "updated_at" field is current whenever an item is added, updated, or deleted.

-- Define the function that updates the timestamp.
CREATE OR REPLACE FUNCTION update_list_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    -- Update the updated_at timestamp of the parent list.
    -- This handles INSERT, UPDATE, and DELETE operations on the items table.
    UPDATE lists
    SET updated_at = NOW()
    WHERE id = COALESCE(NEW.list_id, OLD.list_id);
    RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql;

-- Add a comment to the function.
COMMENT ON FUNCTION update_list_updated_at() IS 'Function to update the updated_at timestamp on the lists table when an item changes.';

-- Create the trigger that executes the function after an item is changed.
CREATE TRIGGER trigger_update_list_on_item_change
AFTER INSERT OR UPDATE OR DELETE ON items
FOR EACH ROW
EXECUTE FUNCTION update_list_updated_at();

-- Add a comment to the trigger.
COMMENT ON TRIGGER trigger_update_list_on_item_change ON items IS 'Updates the list timestamp when an item is inserted, updated, or deleted.';
